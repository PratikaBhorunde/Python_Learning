"""integer data type"""
a = 30
print(a)
print(type(a))

b = -12
print(b)
print(type(b))

"""float data type"""
c = 56.8
print(c)
print(type(c))

d = 12/3
print(d)
print(type(d))

"""complex data type"""
e = 34j #only "j" is used to represent complex numbers
print(e)
print(type(e))

"""string data type"""
name = "pratika"
print(name)
print(type(name))


"""boolean data type"""
f = True
print(f)
print(type(f))

"""unicode"""
g = 65
print(g)
print(chr(g)) #chr() function is used to convert unicode to string

h =  "H"
print(h)
print(ord(h)) #ord() function is used to convert string to unicode
      
"""indexing"""
i = "hello"
print(i[0])
print(i[1])
print(i[2])
print(i[3])
print(i[4])
print(i[-1])
print(i[-2])
print(i[-3])
print(i[-4])
print(i[-5])

"""slicing"""
j = "black pink"
print(j[0:5:1]) #black
print(j[6:10:1]) #pink
